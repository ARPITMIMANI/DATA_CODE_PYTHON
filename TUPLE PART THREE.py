# JOINING TWO TUPLES USING "+" .

a = ( "ONE" , "TWO" , "THREE" , "FOUR" , "FIVE" , "SIX" )
b = (1,2,3,4,5,6)
c = a + b
print(c)

# MULTILCATION OF TUPLE .

a = ( "ONE" , "TWO" , "THREE" , "FOUR" , "FIVE" , "SIX" )
d = a * 3
print(d)

# COPY THE TUPLE USING BUILD IN METHOD tuple() .

a = ( "ONE" , "TWO" , "THREE" , "FOUR" , "FIVE" , "SIX" )
e = tuple(a)
print(e)