# FIRST WAY OF ASSIGNING MANY VALUES TO MANY VARIABLES .

a1 = "NETHERLANDS"
b1 = "CROATIA"
c1 = "ANDORRA"
d1 = "GREECE"

print(a1)
print(b1)
print(c1)
print(d1)

# SECOND WAY OF ASSIGNING MANY VALUES TO MANY VARIABLES .

a1 , b1 , c1 , d1 = "NETHERLANDS" , "CROATIA" , "ANDORRA" , "GREECE"

print(a1)
print(b1)
print(c1)
print(d1)

# ASSIGNING ONE VALUE TO MANY VARIABLES .

a2 = b2 = c2 = d2 = "COUNTRY NAME"

print(a2)
print(b2)
print(c2)
print(d2)

# THIRD WAY OF ASSIGNING MANY VALUES TO MANY VARIABLES .

country = [ "CHILE" , "PANMA" , "BRAZIL" , "SURINAME" , "ARGENTINA" , "JAMAICA" ]
a3 , b3 , c3 , d3 , e3 , f3 = country

print(a3)
print(b3)
print(c3)
print(d3)
print(e3)
print(f3)

